function [ varargout ] = city_info( city_name )
%CITY_INFO Returns information about US cities
%   CITY_INFO() Without arguments, this function returns a vector
%   structure contining fields Name, Longitude, and Latitude for each city.
%   Longitude is defined with west of 0 as negative values.
%
%   CITY_INFO( CITY_NAME ) returns longitude and latitude as separate
%   arguments for the specified city. City name is case insensitive.

E = JLLErrors;

% Define the structure. We will define it as a cell array rather than have
% to type .Longitude and .Latitude repeatedly, but return as structure so
% that it is easy to know what each piece of information is.

fieldnames = {'Name', 'Latitude', 'Longitude'};
city_cell = {'Atlanta', 33.775, -84.32;...
             'Boston', 42.38, -71.02;...
             'Charlotte', 35.34, -80.86;...
             'Chicago', 41.78, -87.68;...
             'Cincinatti', 39.12, -84.50;...
             'Dallas', 32.86, -96.96;...
             'Denver', 39.78, -105.04;...
             'Detriot', 42.26, -83.12;...
             'El Paso', 31.74, -106.38;...
             'Houston', 29.82, -95.28;...
             'Indianapolis', 39.80, -86.12;...
             'Jacksonville', 30.40, -81.60;...
             'Kansas City', 39.10, -94.56;...
             'Las Vegas', 36.18, -115.14;...
             'Los Angeles', 34.06, -117.92;...
             'Louisville', 38.20, -85.74;...
             'Memphis', 35.10, -90.04;...
             'Miami', 26.02, -80.34;...
             'Minneapolis', 44.96, -93.22;...
             'Nashville', 36.14, -86.62;...
             'New Orleans', 29.98, -90.22;...
             'New York', 40.72, -73.80;...
             'Philadelphia', 39.98, -75.16;...
             'Phoenix', 33.54, -112.00;...
             'Portland', 45.44, -122.60;...
             'Richmond', 37.42, -77.30;...
             'Salt Lake City', 40.72, -111.92;...
             'San Antonio', 29.56, -98.44;...
             'San Diego', 32.66, -116.86;...
             'Seattle', 47.42, -122.22;...
             'St. Louis', 38.64, -90.32;...
             'Tampa', 27.90, -82.42;...
             'Tucson', 32.24, -110.88;...
             'Virginia Beach', 36.90, -76.32;...
             'Washington D.C.', 39.20, -76.58};
         
S = cell2struct(city_cell, fieldnames, 2);
if ~exist('city_name', 'var')
    varargout{1} = S;
    return 
else
    if ~ischar(city_name)
        E.badinput('CITY_NAME must be a string, if given')
    elseif ~isfield(S,city_name)
        E.badinput('%s is not a recognized city name, it may not be defined in CITY_INFO.m')
    end
    
    varargout{1} = S.(city_name).Longitude;
    varargout{2} = S.(city_name).Latitude;
end
end

